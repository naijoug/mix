//: ## String Search (字符串搜索算法)

//: Brute-Force String Search (Brute 暴力字符搜索)
//: > 一个简单粗暴的方法


//: Boyer-Moore
//: > 一种高效的字符串子串搜索算法（BM算法）。它不需要对被搜索的字符串中的字符进行逐一比较，而是根据一个查找表跳过其中的某些部分

//: Knuth-Morris-Pratt
//: > 一个线性复杂度字符串搜索算法（KMP算法），通过模式字符进行匹配返回所有的字符串的位置

//: Rabin-Karp
//: > 通过哈希算法快速搜索


//: Longest Common Subsequence (最长公共子序列)
//: > 找到两个字符串中的最长公共子序列


//: Z-Algorithm ()
//: > 在一个字符串中找到所有的模式字符，并返回模式字符在字符串中的开始位置



